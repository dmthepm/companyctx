"""Regression-corpus validation for the M2 envelope.

Every slug in ``REGRESSION_SLUGS`` is pinned as a byte-diff regression case:
running ``companyctx fetch <site> --mock`` must reproduce the committed
``fixtures/<slug>/expected.json`` once ``fetched_at`` is scrubbed. The set
splits into three groups:

- Five synthetic-corpus fixtures spanning all five detectable stacks
  (WordPress, Squarespace, Wix, Webflow, custom/no-marker) across three
  niches — a regression in the extractor, tech-stack detector, or envelope
  serializer trips at least one of them.
- Two FM-7 failure-shape fixtures (`fm7-js-redirect-root`,
  `fm7-maintenance-page`) promoted from the 2026-04-21 durability run — they
  guard the thin/empty `homepage_text` envelope shape.
- One FM-13 network-failure fixture (`fm13-timeout-smb-01`) driven by the
  `fixture-block.txt` sentinel (see issue #40) — it locks the degraded
  envelope shape for network-level failures that produce no HTML.

This is intentionally a regression snapshot, not a real golden oracle. Real
golden validation against the external brief pipeline's output is a separate
v0.1.0 release gate because those expected outputs must come from an external
source, not from `companyctx.core.run()` itself.

The remaining 25 synthetic fixtures still have committed ``expected.json``
files generated by the same orchestrator; they're exercised looser by
``tests/test_fixtures_corpus.py``.

Rationale for each subset's picking lives in ``fixtures/README.md`` under
"Regression corpus".
"""


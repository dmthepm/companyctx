def _scrub_fetched_at(raw: bytes) -> bytes:
    return FETCHED_AT_RE.sub(b'"fetched_at": "<scrubbed>"', raw)


@pytest.mark.parametrize("slug", REGRESSION_SLUGS)
def test_regression_fixture_matches_expected_envelope_bytes(slug: str) -> None:
    expected_path = FIXTURES_DIR / slug / "expected.json"
    expected = _scrub_fetched_at(expected_path.read_bytes())

    runner = CliRunner()
    result = runner.invoke(
        app,
        [
            "fetch",
            f"{slug}.example",
            "--mock",
            "--json",
            "--fixtures-dir",
            str(FIXTURES_DIR),
        ],
    )
    assert result.exit_code == 0, result.stdout
    assert _scrub_fetched_at(result.stdout.encode("utf-8")) == expected, slug
